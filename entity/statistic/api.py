from quart import Blueprint, request, jsonify
from app_init.app_init import entity_service, cyoda_token
from common.config.config import ENTITY_VERSION
import uuid
import asyncio

api_bp_statistic = Blueprint('api/statistic', __name__)

ENTITY_MODEL = 'statistic'

@api_bp_statistic.route('/environments/<string:id>/statistics', methods=['GET'])
async def get_id(id):
    """Retrieve statistic information."""
    try:
        data = await entity_service.get_item(
            token=cyoda_token,
            entity_model=ENTITY_MODEL,
            entity_version=ENTITY_VERSION,
            technical_id=id
        )
        return jsonify({"data": data}), 200
    except Exception as e:
        return jsonify({"error": str(e)}), 500

